const I18N = {
  current: 'en',

  init() {
    const saved = localStorage.getItem('krg-lang');
    const browserLang = (navigator.language || 'en').split('-')[0];
    const supported = ['en', 'es', 'pt'];

    if (saved && supported.includes(saved)) {
      this.current = saved;
    } else if (supported.includes(browserLang) && browserLang !== 'en') {
      this.current = browserLang;
    } else {
      this.current = 'en';
    }

    this.apply();
    this.bindSwitcher();
  },

  apply() {
    document.documentElement.lang = this.current;
    const dict = translations[this.current] || translations.en;

    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (dict[key] !== undefined) {
        el.textContent = dict[key];
      }
    });

    const codeEl = document.getElementById('lang-current-code');
    if (codeEl) codeEl.textContent = this.current.toUpperCase();

    document.querySelectorAll('.lang-menu li').forEach(li => {
      li.classList.toggle('active', li.dataset.lang === this.current);
    });
  },

  set(lang) {
    if (!translations[lang]) return;
    this.current = lang;
    localStorage.setItem('krg-lang', lang);
    this.apply();
  },

  bindSwitcher() {
    const switcher = document.querySelector('.lang-switcher');
    if (!switcher) return;

    const toggle = switcher.querySelector('.lang-current');
    const menu = switcher.querySelector('.lang-menu');

    toggle.addEventListener('click', (e) => {
      e.stopPropagation();
      switcher.classList.toggle('open');
    });

    menu.addEventListener('click', (e) => {
      const li = e.target.closest('li');
      if (!li) return;
      this.set(li.dataset.lang);
      switcher.classList.remove('open');
    });

    document.addEventListener('click', () => {
      switcher.classList.remove('open');
    });
  }
};

document.addEventListener('DOMContentLoaded', () => I18N.init());
